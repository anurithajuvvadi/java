package java_tutorial;

public class vowel {

	public static void main(String[] args) {
char c='a';
if(c == 'a' || c == 'e' || c == 'i' || c == 'o' || c == 'u') {
	System.out.println("it is a vowel");
}
else if(c == 'A' || c == 'E' || c == 'I' || c == 'O' || c == 'U') {
	System.out.println("it is a vowel");
}
else {
	System.out.println("it is a consonant");

}
	}

}
